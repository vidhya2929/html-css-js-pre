let number =Number(prompt("Enter a number between 1 to 7"));
switch(number){
  case 1:
    console.log("It is Monday");
    alert("Monday");
    break;
  case 2:
    console.log("It is Tuesday");
    alert("Tuesday");
    break;
  case 3:
    console.log("It is Wednesday");
    alert("Wednesday");
    break;
  case 4:
    console.log("It is Thursday");
    alert("Thursay");
    break;
  case 5:
    console.log("It is Friday");
    alert("Friday");
    break;
  case 6:
    console.log("It is Saturday");
    alert("Saturday");
    break;
  case 7:
    console.log("It is Sunday");
    alert("Sunday");
    break;
  default:
    console.log("Invalid ENTRY");
    alert("Invalid");
}

